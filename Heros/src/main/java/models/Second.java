package models;

public class Second {
    private String name;
    private String second;
    private String status;

    public Second(String name, String second, String status) {
        this.name = name;
        this.second = second;
        this.status = status;
    }

    public String getName() {
        return name;
    }
    public String getSecond() {
        return second;
    }
    public String getStatus() {
        return status;
    }

}


